#include<stdio.h>
int operator3(){
    int a = 5,c;

    c=a;
    printf("c = %d\n",c);
    c+=a;
    printf("c = %d\n",c);
    c-=a;
    printf("c = %d\n",c);
    c*=a;
    printf("c = %d\n",c);
    c/=a;
    printf("c = %d\n",c);
    c%=a;
    printf("c = %d\n",c);
}
